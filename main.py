assert tree2list(list2tree(t)) == t


class Solution:
    def run(self, triangle: List[List[int]]) -> int:
        endi = len(triangle)
        acc = [[0 for _ in range(i + 1)] for i in range(endi + 1)]
        for i in range(endi - 1, -1, -1):
            ii = i + 1
            for j in range(i + 1):
                acc[i][j] = min(acc[ii][j], acc[ii][j + 1]) + triangle[i][j]
                pass

        return acc[0][0]


